package com.xworkz.washmachine;

public class WashingMachine {

	private String brand;
	private String color;

	public WashingMachine(String brand, String color) {
		super();
		this.brand = brand;
		this.color = color;
	}

	public String getBrand() {
		return brand;
	}

	public String getColor() {
		return color;
	}

}
